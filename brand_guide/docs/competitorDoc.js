/**
 * competitorDoc.js
 * Skill: Write the Competitor Analysis document to Notion.
 *
 * Takes competitor data and creates a structured competitor intelligence
 * page in your Notion database — one page per competitor.
 */

import {
  createPage,
  heading1,
  heading2,
  heading3,
  paragraph,
  bulletList,
  divider,
} from "../../utils/notion.js";
import { config } from "../../config/settings.js";

/**
 * Write a single competitor's analysis to Notion.
 * @param {object} competitor - output from analyzeCompetitor()
 * @returns {Promise<object>} created Notion page
 */
export async function writeCompetitorToNotion(competitor) {
  console.log(`  Writing competitor "${competitor.name}" to Notion...`);

  const blocks = [
    heading1(`Competitor: ${competitor.name}`),
    paragraph(`Platform: ${competitor.platform} | Handle: @${competitor.handle}`),
    paragraph(`Last updated: ${new Date().toLocaleDateString()}`),
    divider(),

    heading2("Profile Overview"),
    ...bulletList([
      `Followers: ${competitor.followers}`,
      `Avg Engagement per Post: ${competitor.avgEngagement}`,
      `Posting Frequency: ${competitor.postingFrequency}`,
      `Bio: ${competitor.bio || "N/A"}`,
    ]),
    divider(),

    heading2("Content Mix"),
    ...bulletList(
      Object.entries(competitor.topContentTypes || {}).map(
        ([type, count]) => `${type}: ${count} posts`
      ).length > 0
        ? Object.entries(competitor.topContentTypes || {}).map(
            ([type, count]) => `${type}: ${count} posts`
          )
        : ["No content type data available"]
    ),
    divider(),

    heading2("Web Intelligence"),
    paragraph(competitor.webInsights?.summary || "No web data found."),
    ...bulletList(
      (competitor.webInsights?.sources || []).map((s) => `${s.title} — ${s.url}`)
    ),
    divider(),

    heading2("Strategic Notes"),
    ...bulletList([
      `Strengths: [Fill in]`,
      `Weaknesses: [Fill in]`,
      `Content Gaps: [Fill in]`,
      `Differentiation Opportunity: [Fill in]`,
    ]),
    divider(),

    paragraph("Generated by Claude Content Machine"),
  ];

  const page = await createPage(
    config.notion.databases.competitor,
    `Competitor — ${competitor.name}`,
    blocks,
    {
      Status: {
        select: { name: "Draft" },
      },
    }
  );

  console.log(`  Created page for ${competitor.name}! ID: ${page.id}`);
  return page;
}

/**
 * Write all competitors to Notion (one page each).
 * @param {object} competitorReport - output from analyzeCompetitors()
 * @returns {Promise<object[]>}
 */
export async function writeAllCompetitorsToNotion(competitorReport) {
  console.log(`\n[Competitor Doc] Writing ${competitorReport.competitors.length} competitors to Notion...`);

  const pages = [];
  for (const competitor of competitorReport.competitors) {
    const page = await writeCompetitorToNotion(competitor);
    pages.push(page);
  }

  console.log(`[Competitor Doc] Done! ${pages.length} pages created in Notion.`);
  return pages;
}
