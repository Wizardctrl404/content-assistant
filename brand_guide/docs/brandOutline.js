/**
 * brandOutline.js
 * Skill: Compile all brand guide data and write the Brand Outline doc to Notion.
 *
 * Takes the output from all other brand guide skills and creates a
 * comprehensive, structured Brand Outline page in your Notion database.
 */

import { createPage, heading1, heading2, paragraph, bulletList, divider } from "../../utils/notion.js";
import { config } from "../../config/settings.js";

/**
 * Write the Brand Outline document to Notion.
 * @param {object} brandData - compiled output from all brand guide skills
 * @returns {Promise<object>} created Notion page
 */
export async function writeBrandOutlineToNotion(brandData) {
  const {
    brandName,
    originStory,
    opinionsViews,
    knowledge,
    rapidFireQA,
    niche,
  } = brandData;

  console.log(`\n[Brand Outline Doc] Writing to Notion for "${brandName}"...`);

  const blocks = [
    // Header
    heading1(`Brand Outline: ${brandName}`),
    paragraph(`Last updated: ${new Date().toLocaleDateString()}`),
    divider(),

    // 1. Origin Story
    heading2("1. Origin Story"),
    paragraph(originStory?.summary || "No origin story data yet."),
    heading2("Story Framework"),
    ...bulletList([
      `Who They Are: ${originStory?.template?.whoTheyAre || "TBD"}`,
      `The Problem: ${originStory?.template?.theProblem || "TBD"}`,
      `The Shift: ${originStory?.template?.theShift || "TBD"}`,
      `The Mission: ${originStory?.template?.theMission || "TBD"}`,
      `The Proof: ${originStory?.template?.theProof || "TBD"}`,
    ]),
    divider(),

    // 2. Opinions & Views
    heading2("2. Opinions & Views"),
    ...bulletList([
      `Core Beliefs: ${opinionsViews?.template?.coreBeliefs || "TBD"}`,
      `Stands Against: ${opinionsViews?.template?.standAgainst || "TBD"}`,
      `Unique Angles: ${opinionsViews?.template?.uniqueAngles || "TBD"}`,
      `Voice & Tone: ${opinionsViews?.template?.voiceTone || "TBD"}`,
    ]),
    divider(),

    // 3. Knowledge & Expertise
    heading2("3. Knowledge & Expertise"),
    ...bulletList([
      `Core Pillars: ${knowledge?.template?.corePillars || "TBD"}`,
      `Frameworks: ${knowledge?.template?.frameworks || "TBD"}`,
      `Audience Level: ${knowledge?.template?.audienceLevel || "TBD"}`,
      `Signature Content: ${knowledge?.template?.signatureContent || "TBD"}`,
    ]),
    divider(),

    // 4. Niche Positioning
    heading2("4. Niche Positioning"),
    ...bulletList([
      `Broad Category: ${niche?.template?.broadCategory || "TBD"}`,
      `Sub-Niche: ${niche?.template?.subNiche || "TBD"}`,
      `Target Demographics: ${niche?.template?.targetAudience?.demographics || "TBD"}`,
      `Psychographics: ${niche?.template?.targetAudience?.psychographics || "TBD"}`,
      `Pain Points: ${niche?.template?.targetAudience?.painPoints || "TBD"}`,
      `Desires: ${niche?.template?.targetAudience?.desires || "TBD"}`,
      `Unique Angle: ${niche?.template?.uniqueAngle || "TBD"}`,
      `Content Language: ${niche?.template?.contentLanguage || "TBD"}`,
    ]),
    divider(),

    // 5. Original Frameworks & IP
    heading2("5. Original Frameworks & IP"),
    ...bulletList(
      (brandData.frameworks || []).length > 0
        ? brandData.frameworks.map(f => `${f.name}: ${f.definition}`)
        : ["No frameworks captured yet — run /belief-mining."]
    ),
    divider(),

    // 6. Hot Takes
    heading2("6. Hot Takes"),
    ...bulletList(
      (brandData.hotTakes || []).length > 0
        ? brandData.hotTakes
        : ["No hot takes captured yet — run /hot-takes."]
    ),
    divider(),

    // 7. Voice Guide
    heading2("7. Voice Guide"),
    ...bulletList([
      `Sounds like: ${brandData.voice?.soundsLike || "TBD"}`,
      `Always: ${brandData.voice?.always || "TBD"}`,
      `Never: ${brandData.voice?.never || "TBD"}`,
      `Signature phrases: ${(brandData.voice?.signaturePhrases || []).join(" / ") || "TBD"}`,
    ]),
    divider(),

    // 8. Rapid Fire Q&A
    heading2("8. Rapid Fire Q&A"),
    paragraph(`Total questions captured: ${rapidFireQA?.totalQuestions || 0}`),
    ...bulletList(
      (rapidFireQA?.questionsFromComments || []).slice(0, 8).length > 0
        ? (rapidFireQA?.questionsFromComments || []).slice(0, 8)
        : ["No questions captured yet — run /rapidfire."]
    ),
    divider(),

    // Footer
    paragraph("Generated by Claude Content Machine"),
  ];

  const page = await createPage(
    config.notion.databases.brandOutline,
    `Brand Outline — ${brandName}`,
    blocks,
    {
      Status: {
        select: { name: "Draft" },
      },
    }
  );

  console.log(`[Brand Outline Doc] Created! Page ID: ${page.id}`);
  console.log(`View it at: https://notion.so/${page.id.replace(/-/g, "")}`);

  return page;
}
